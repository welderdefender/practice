package beginner;

/*
Complete the method that takes a boolean value and return a "Yes" string for true, or a "No" string for false.

Ссылка на задачу: https://www.codewars.com/kata/53369039d7ab3ac506000467/train/java
 */

public class ConvertBooleanValuesToStringsYesNo {
    public static String boolToWord(boolean b) {
        return b ? "Yes" : "No";
    }
}